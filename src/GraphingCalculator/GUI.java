    private double computeSum (double coef[], double exp[], double x){
        double sum = 0;
        for (int i = 0; i < coef.length; i++){
            sum += (coeff[i]*Math.pow(x,exp[i]));
        }
        return sum;
    }
    
    private void graph(double coef[], double exp[], boolean derive){
        int z = -6;
        int a = 6;
        Graphics g = jPanel2.getGraphics();
        g.drawLine(150, 0, 150, 300);       //Draw axis
        g.drawLine(0, 150, 300 , 150);      //
        for (int i = 0; i < 520; i = i + 25){
            g.drawLine (i, 145, i, 155);   //Draw ticks
            g.drawLine (145, i , 155, i);   //
            if(z != 0 || a != 0){
                g.drawString(String.valueOf(z),i-4,165);    //Draw numbers
                g.drawString(String.valueOf(a),155,i+6);    //
            }
            z++;
            a--;
        }
        double x = -150;
        double y;
        do {
            y = computeSum(coef, exp, x);//Equation for y
            x = x + 0.001;
            if(derive == false){
                g.setColor(Color.red);
            }else{
                g.setColor(Color.blue);
            }
            double drawx = (x+6)*25;
            double drawy = (-y+6)*25;
            g.fillRect((int)drawx, (int)drawy, 2, 2);        //Draw dot on coordinate
        }while (x <= 100);
    }
    
